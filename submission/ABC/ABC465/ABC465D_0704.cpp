/**
 * 1ケースぶんの処理実行
 */
void solve()
{
    ll X, Y, K;
    cin >> X >> Y >> K;
    ll x = X;
    ll countX = 0;
    ll y = Y;
    ll countY = 0;
    while (x != y)
    {
        while (x < y)
        {
            ++countY;
            y /= K;
        }
        while (y < x)
        {
            ++countX;
            x /= K;
        }
    }
    cout << countX + countY << endl;
}

